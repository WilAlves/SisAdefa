<font color="blue" >A deficiência e vários tipos existentes</font>
<br><br>
<font><Font color="blue"><b><i>Qual o significado da palavra “deficiência”?</Font></b></i><br /><br />
  Segundo a Organização Mundial de Saúde, deficiência é o substantivo atribuído a toda a perda ou anormalidade de uma estrutura ou função psicológica, fisiológica ou anatómica. Refere-se, portanto, à biologia do ser humano.<br />
<br />
<br />
<Font color="blue"><b><i>Quem pode ser considerado deficiente?</Font></b></i><br /> <br />
  A expressão “pessoa com deficiência” pode ser atribuída a pessoas portadoras de qualquer tipo(s) de deficiência. Porém, em termos legais, esta mesma expressão é aplicada de um modo mais restrito e refere-se a pessoas que se encontram sob o amparo de determinada legislação.<br />

  É designado “deficiente” todo aquele que tem um ou mais problemas de funcionamento ou falta de parte anatómica, embargando com isto dificuldades a vários níveis: de locomoção, percepção, pensamento ou relação social.<br />
  Até bem recentemente, o termo “deficiente” era vulgarmente aplicado a pessoas portadoras de deficiência(s). Porém, esta expressão embarga consigo uma forte carga negativa depreciativa da pessoa, pelo que foi, ao longo dos anos, cada vez mais rejeitada pelos especialistas da área e, em especial, pelos próprios portadores. Actualmente, a palavra é considerada como inadequada e estimuladora do preconceito a respeito do valor integral da pessoa. Deste modo, a substitui-la surge a expressão: “pessoa especial”.<br />
<br />
<br />
<Font color="blue"><b><i>Quais os vários tipos de deficiência?</Font></b></i><br /><br />
  A pessoa especial pode ser portadora de deficiência única ou de deficiência múltipla (associação de uma ou mais deficiências). As várias deficiências podem agrupar-se em quatro conjuntos distintos, sendo eles:<br />
<br />
  •Deficiência visual<br />

  •Deficiência motora<br />
  •Deficiência mental<br />
  •Deficiência auditiva<br />
